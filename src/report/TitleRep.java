
package report;

public class TitleRep {
 
    private String title;
    private String value;

    public TitleRep(String title, String value) {
       this.title = title; 
       this.title = value;
    }
    
    public String getTitle() {
        return title;
    }

    public String getValue() {
        return value;
    }
     
}
