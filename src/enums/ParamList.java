    public Defparam defparam = LambdaPar.def_Default;
    public Dictionary dictionary = LambdaPar.dic_DEFAULT;
    public Formatter formatter = LambdaPar.frm_Default;
    public Checkparam check = LambdaPar.check_STRING;

    ParamList(int numb, String text) {
        this.grup = numb;
        this.text = text;
    }

    ParamList(int numb, String text, Object... obj) {
        this.grup = numb;
        this.text = text;
        for (Object o : obj) {
            if (o instanceof Dictionary) {
                this.dictionary = (Dictionary) o;
            } else if (o instanceof Checkparam) {
                this.check = (Checkparam) o;
            } else if (o instanceof Formatter) {
                this.formatter = (Formatter) o;
            } else if (o instanceof Defparam) {
                this.defparam = (Defparam) o;
            }
        }
    }


    public static ParamList find(int grup) {
        return Arrays.asList(ParamList.values()).stream().filter(en -> en.grup == grup).findFirst().orElse(null);
    }

    
